package com.acensi.gineste.model;

public class Mars {

    // x
    private final int length;
    // y
    private final int height;

    public Mars(int length, int height) {
        this.length = length;
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public int getHeight() {
        return height;
    }
}
